function titleCase(value: string): string {
  return value
    .split(/[_-]+/)
    .filter((token) => token.length > 0)
    .map((token) => token[0].toUpperCase() + token.slice(1))
    .join(" ");
}

export function buildTopologyNodeDefinitions(topology: T1ClusterTopology): TopologyNodeUiDefinition[] {
  if (topology.nodeDefinitions && topology.nodeDefinitions.length > 0) {
    return topology.nodeDefinitions.map((node) => ({ ...node }));
  }

  const baseName = titleCase(topology.id);
  const customNames = CULTIVATION_NODE_NAMES[topology.id];
  const resonanceNodeIds = new Set((topology.specialNodes ?? []).map((node) => node.id));
  return topology.nodes.map((node) => ({
    id: node.id,
    name: customNames?.[node.id] ?? `${baseName} Node ${node.id + 1}`,
    // Keep one visible anchor in purely non-source topologies for the UI layer.
    unlocked: node.isSourceNode || node.id === 0,
    si: 0,
    unlockCost: node.isSourceNode || node.id === 0 ? 0 : 100 * (node.id + 1),
    nodeType: String(node.type),
    isSourceNode: node.isSourceNode,
    nodeShape: node.isSourceNode
      ? "star"
      : resonanceNodeIds.has(node.id)
        ? "square"
        : (node.type === T1NodeType.IO_IN || node.type === T1NodeType.IO_OUT || node.type === T1NodeType.IO_BIDIR)
          ? "diamond"
          : "circle",
    canProject: false
  }));
}

